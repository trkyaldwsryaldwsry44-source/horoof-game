// مساعد: يجيب سؤال عشوائي من حرف معين بدون تكرار قدر الإمكان
window.pickQuestion = function(letter, usedSet) {
  const arr = window.QUESTION_BANK[letter] || [];
  if (arr.length === 0) return { q: "ما فيه أسئلة لهذا الحرف حالياً.", a: "—" };

  // جرّب تجيب شي ما تكرر
  const candidates = arr.filter((_, idx) => !usedSet.has(letter + ":" + idx));
  const pool = candidates.length ? candidates : arr;

  const chosen = pool[Math.floor(Math.random() * pool.length)];
  const idx = arr.indexOf(chosen);
  usedSet.add(letter + ":" + idx);
  return chosen;
};
